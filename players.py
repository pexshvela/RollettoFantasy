def _stable_id(name: str, pos: str) -> str:
    """Generate a stable ID from name so it never changes when list order changes."""
    import hashlib
    key = (pos + "_" + name).lower().encode()
    return pos.lower() + "_" + hashlib.md5(key).hexdigest()[:8]


